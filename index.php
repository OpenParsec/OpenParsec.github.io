     <tr nosave>
      <td nosave bgcolor="black"><font face="Helvetica,Arial"><br>
		Feb. 09, 2012 -- As I find myself writing this entry on openparsec.sourceforge.net, I can't believe 
		this is happening.  Nearly 9 years after the release of the source code by its original creators, 
		there are noticable things going on at the 
		<a target="_blank" href="http://sourceforge.net/projects/openparsec/">Source Forge Project page</a>.   I'm Jason, (AKA: Uber Linux Guy.)
		I've worked on the Open Parsec code off and on ever since the code was released.  And although I am
		not one of the original creators, I do have a passion to see this game complete. I had gone so far 
		as to spool up an SVN repository on my personal server along with a mailing list for anyone that wanted to help, 
		mostly because the SF Project page fizzled and the original creators all moved on to bigger and 
		better things. (And rightfully so.)<br /><br />
	
		Recently, after being away from the code for a couple of years again, due mostly to work, I 
		received an <a href="http://tulg.org/pipermail/openparsec/2012-January/000024.html">email</a>
		to the list on my personal server from Phil Spencer.  Phil's interest in the game and his email 
		inspired me to get back into things.  We've been twiddling with the code here and there for 
		about a month now, and after an email to the Open Parsec Fathers, they were kind enough to 
		allow me Project Admin rights to take up the reigns on the SF page.  <br /><br />

		Being someone who doesn't like to let others down, and now with the power to help make things happen, 
		I am making the call to all those developers out there who have stumbled across this work of gaming 
		art and always wanted to see it to completion.  Please join us on the 
		<a href="http://sourceforge.net/projects/openparsec/">Source Forge Project page</a>, throw your development 
		skills into the ring, and let's see if we can get a fully functional Internet version of the game 
		out before the 10th anniversary of the Open Source Release of Parsec. <br /><br />

		And remember, "There Is No Safe Distance."<br /><br />
		-- Jason (THE Uber Linux Guy)
